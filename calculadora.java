
public class calculadora extends superclasse {

	int num1;
	
	int num2;
	
	public calculadora(int num1, int num2){
		
		this.num1=num1;
		
		this.num2=num2;
		
	}
	
	
	public int sumar(){
		
		return num1+num2;
		
	}
	
	public int multiplicar(){
		
		return num1*num2;
		
	}
}
